"""flow_agent 的 prompt：指导 LLM 将模式卡片展开为分阶段思维流。"""

from code.backend.services.simulation import PHASES


def _build_phase_description() -> str:
    """从集中化阶段定义动态生成 prompt 中的阶段描述。"""
    lines = []
    for p in PHASES:
        lines.append(f"- {p['name']}：{p['description']}")
    return "\n".join(lines)


